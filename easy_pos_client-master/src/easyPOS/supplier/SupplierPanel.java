    private void jButton35ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton35ActionPerformed
        boolean permission=true;
        if(jTextFieldSuppNetTot1.getText().equals("") || jTextFieldSuppCode1.getText().equals("") || jTextFieldSuppInvNo1.getText().equals("")){
            permission=false;
            JOptionPane.showMessageDialog(this, "Fill the required fields ! !");
        }else{
        }
        boolean result1=supp.checkSupplierExist(jTextFieldSuppCode1.getText());
        if(jCheckBoxSuppAlert1.isSelected()){
            if(jTextFieldSuppAlertDate1.getText().equals("")){
                permission=false;
                JOptionPane.showMessageDialog(this, "Set the Alert date !");
            }
        }else{
        }
        if(result1==false ){
            permission=false;
            JOptionPane.showMessageDialog(this, "Supplieer code does not exist !");
        }else{
        }

        if(permission==true ){
            SupplyVariables suppVar=new SupplyVariables();
            try{
                suppVar.setDate(DateTimeUtil.dateFormatForDB(jTextFieldSuppDate1.getText()));
                suppVar.setSupplier_code(jTextFieldSuppCode1.getText());

                suppVar.setInvoice_no(jTextFieldSuppInvNo1.getText());
                suppVar.setTotal(Double.parseDouble(jTextFieldSuppTot1.getText()));
                suppVar.setDiscount(Double.parseDouble(jTextFieldSuppDiss1.getText()));
                suppVar.setNet_total(Double.parseDouble(jTextFieldSuppNetTot1.getText()));

                if(jCheckBoxSuppAlert1.isSelected()){
                    suppVar.setSet_alert(1);
                }else{
                    suppVar.setSet_alert(0);
                }

                if(jTextFieldSuppPaid1.getText().equals("")){
                    suppVar.setPaid(Double.parseDouble(jTextFieldSuppNetTot1.getText()));
                }else{
                    suppVar.setPaid(Double.parseDouble(jTextFieldSuppPaid1.getText()));
                }

                if(jTextFieldSuppBal1.getText().equals("")){
                    suppVar.setBalance(0.00);
                }else{
                    suppVar.setBalance(Double.parseDouble(jTextFieldSuppBal1.getText()));
                }
                suppVar.setDue_to_pay(jTextFieldSuppDueToPay1.getText());
                suppVar.setAlert_date(jTextFieldSuppAlertDate1.getText());

                suppVar.setEditBy(ApplicationDataManager.getInstance().getLoggedInUser().getUserName());
                suppVar.setInputMethod("auto");

                boolean result2=supp.addSupply(suppVar);//insert to supplies table
                if(result2==true){
                    JOptionPane.showMessageDialog(this, "Successfully inserted !");
                    boolean res=supp.deleteTemp(jTextFieldSuppInvNo1.getText());//delete temp
                    if(res==true){
                        //clear fields
                        jTextFieldSuppInvNo1.setText("");
                        jTextFieldSuppCode1.setText("");
                        jTextFieldSuppName1.setText("");
                        jTextFieldSuppTot1.setText("");
                        jTextFieldSuppDiss1.setText("");
                        jTextFieldSuppNetTot1.setText("");
                        jCheckBoxSuppAlert1.setSelected(false);
                        jTextFieldSuppPaid1.setText("");
                        jTextFieldSuppBal1.setText("");
                        jTextFieldSuppDueToPay1.setText("");
                        jTextFieldSuppAlertDate1.setText("");
                        jCheckBoxAutoInv.setSelected(false);
                    }else{
                        JOptionPane.showMessageDialog(this, "Error occur while deleting temporary invoice!");
                    }
                    suppliesLoadTableForSuppliers(0,"","","");
                }else{
                    JOptionPane.showMessageDialog(this, "Error occur while storing data !");
                }

            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Invalid input !");
            }

        } // TODO add your handling code here:
    }//GEN-LAST:event_jButton35ActionPerformed

